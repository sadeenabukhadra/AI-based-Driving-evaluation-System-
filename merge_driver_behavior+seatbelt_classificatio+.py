import os
import numpy as np
import tensorflow as tf
import matplotlib.pyplot as plt
from PIL import Image
from google.colab import drive, files
from ultralytics import YOLO
import arabic_reshaper
from bidi.algorithm import get_display

# دالة لتصحيح عرض النص العربي
def ar_text(text):
    return get_display(arabic_reshaper.reshape(text))

# 1) ربط Google Drive
drive.mount('/content/drive')

# 2) المسارات
YOLO_MODEL_PATH = "/content/drive/MyDrive/runs_driver/driver_safety_model_fixed/weights/best.pt"
SEATBELT_MODEL_PATH = "/content/drive/MyDrive/seatbelt_project_strong/seatbelt_classifier_final.keras"
CLASS_NAMES_PATH = "/content/drive/MyDrive/seatbelt_project_strong/class_names.txt"
THRESHOLD_PATH = "/content/drive/MyDrive/seatbelt_project_strong/best_threshold.npy"

# 3) تحميل الموديلات
yolo_model = YOLO(YOLO_MODEL_PATH)
seatbelt_model = tf.keras.models.load_model(SEATBELT_MODEL_PATH)

with open(CLASS_NAMES_PATH, "r") as f:
    seatbelt_classes = [line.strip() for line in f.readlines()]

best_threshold = float(np.load(THRESHOLD_PATH)) if os.path.exists(THRESHOLD_PATH) else 0.5

print("تم تحميل موديل السلوك ✅")
print("تم تحميل موديل الحزام ✅")
print("كلاسات الحزام:", seatbelt_classes)
print("Threshold:", best_threshold)

# 4) الكلاسات الجديدة
class_map = {
    0: "improper posture",
    1: "safe driving",
    2: "drowsy",
    3: "sleeping",
    4: "slight distraction",
    5: "phone usage",
    6: "talking on phone",
    7: "radio distraction",
    8: "drinking",
    9: "reaching side object",
    10: "mirror distraction",
    11: "talking to passenger",
    12: "multiple distractions"
}

# 5) ترجمة الكلاسات للعربي
class_map_ar = {
    "improper posture": "وضعية جلوس أو قيادة غير صحيحة",
    "safe driving": "قيادة آمنة",
    "drowsy": "نعاس",
    "sleeping": "نوم أثناء القيادة",
    "slight distraction": "تشتت بسيط",
    "phone usage": "استخدام الهاتف",
    "talking on phone": "التحدث على الهاتف",
    "radio distraction": "تشتت بسبب الراديو أو لوحة التحكم",
    "drinking": "شرب أثناء القيادة",
    "reaching side object": "محاولة الوصول إلى جسم جانبي",
    "mirror distraction": "تشتت بسبب المرآة",
    "talking to passenger": "التحدث مع الراكب",
    "multiple distractions": "تشتتات متعددة"
}

seatbelt_map_ar = {
    "seatbelt": "لابس حزام الأمان",
    "noSeatbelt": "غير لابس حزام الأمان"
}

# 6) دالة توقع الحزام
def predict_seatbelt(image_path, model, class_names, threshold=0.5, img_size=(300, 300)):
    img = tf.keras.utils.load_img(image_path, target_size=img_size)
    img_array = tf.keras.utils.img_to_array(img)
    img_array = np.expand_dims(img_array, axis=0)

    prob = model.predict(img_array, verbose=0)[0][0]
    pred_idx = 1 if prob >= threshold else 0
    pred_class_en = class_names[pred_idx]
    pred_class_ar = seatbelt_map_ar.get(pred_class_en, pred_class_en)
    confidence = float(prob) if pred_idx == 1 else float(1 - prob)

    return pred_class_ar, confidence, float(prob)

# 7) دالة توقع سلوك السائق
def predict_driver_action(image_path, model):
    results = model(image_path, verbose=False, conf=0.20)
    result = results[0]

    if result.boxes is None or len(result.boxes) == 0:
        return "unknown", "لم يتم التعرف على سلوك السائق", 0.0, None

    confs = result.boxes.conf.cpu().numpy()
    clss = result.boxes.cls.cpu().numpy().astype(int)

    best_idx = int(np.argmax(confs))
    best_class_id = int(clss[best_idx])
    best_conf = float(confs[best_idx])

    action_en = class_map.get(best_class_id, f"class_{best_class_id}")
    action_ar = class_map_ar.get(action_en, action_en)

    return action_en, action_ar, best_conf, best_class_id

# 8) دمج النتيجتين
def predict_all(image_path):
    action_en, action_ar, action_conf, action_id = predict_driver_action(image_path, yolo_model)
    seatbelt_ar, seatbelt_conf, raw_prob = predict_seatbelt(
        image_path,
        seatbelt_model,
        seatbelt_classes,
        threshold=best_threshold,
        img_size=(300, 300)
    )

    img = Image.open(image_path)
    plt.figure(figsize=(8, 8))
    plt.imshow(img)
    plt.axis("off")

    title_text = (
        f"سلوك السائق: {action_ar} ({action_conf:.2%})\n"
        f"حالة الحزام: {seatbelt_ar} ({seatbelt_conf:.2%})"
    )
    plt.title(ar_text(title_text), fontsize=14)

    plt.show()

    print("========== النتيجة النهائية ==========")
    print("سلوك السائق بالعربي:", action_ar)
    print("سلوك السائق بالإنجليزي:", action_en)
    print("رقم الكلاس:", action_id)
    print("ثقة سلوك السائق:", round(action_conf, 4))
    print("حالة الحزام:", seatbelt_ar)
    print("ثقة الحزام:", round(seatbelt_conf, 4))
    print("الاحتمال الخام للحزام:", round(raw_prob, 4))

